def add_rel(code, opcode, target_addr, base_addr):
    curr_addr = base_addr + len(code)
    disp = target_addr - (curr_addr + 2)
    if disp < -128 or disp > 127: raise ValueError(f"Branch out of range: {disp}")
    code.extend([opcode, disp & 0xFF])

def rle_compress(data):
    result = bytearray()
    i, n = 0, len(data)
    while i < n:
        if i + 1 < n and data[i] == data[i + 1]:
            val = data[i]; run = 1
            while i + run < n and run < 129 and data[i + run] == val: run += 1
            result.append(run + 126); result.append(val); i += run
        else:
            start = i
            while i < n and (i - start) < 128:
                if i + 1 < n and data[i] == data[i + 1]: break
                i += 1
            lit_len = i - start
            result.append(lit_len - 1); result.extend(data[start:start + lit_len])
    return bytes(result)

def _build_streamer_code(base_addr):
    code = []
    # 1. System Setup
    code += [0x78, 0xD8] # SEI, CLD
    code += [0xEE, 0x20, 0xD0, 0xEE, 0x20, 0xD0] # PULSE
    code += [0xA9, 0x35, 0x85, 0x01] # BASIC OFF
    code += [0xA9, 0x00, 0x8D, 0x20, 0xD0, 0x8D, 0x21, 0xD0] # BLACK
    code += [0xAD, 0x02, 0xDD, 0x09, 0x03, 0x8D, 0x02, 0xDD] # DDR
    code += [0xAD, 0x00, 0xDD, 0x29, 0xFC, 0x09, 0x03, 0x8D, 0x00, 0xDD] # Bank 0
    code += [0xA9, 0x00, 0x85, 0x03, 0x85, 0x0E, 0x85, 0x0F] # Counter

    jmp_main_idx = len(code)
    code += [0x4C, 0x00, 0x00] # JMP main

    # --- ef3usb_fread ---
    fread_addr = base_addr + len(code)
    code.extend([0x2C, 0x09, 0xDE, 0x50, 0xFB, 0x8E, 0x0A, 0xDE, 0x2C, 0x09, 0xDE, 0x50, 0xFB, 0x8C, 0x0A, 0xDE]) # TXE
    code.extend([0x2C, 0x09, 0xDE, 0x10, 0xFB, 0xAD, 0x0A, 0xDE, 0x2C, 0x09, 0xDE, 0x10, 0xFB, 0xAD, 0x0A, 0xDE]) # RXF
    code.extend([0x98, 0x85, 0xF2, 0x8A, 0x85, 0xF3, 0xA0, 0x00]) # Count
    fread_loop = base_addr + len(code)
    code.extend([0x2C, 0x09, 0xDE, 0x10, 0xFB]) # Wait RX
    code.extend([0xAD, 0x0A, 0xDE, 0x91, 0xFC]) # LDA, STA ($FC),Y
    code.extend([0xC8, 0xD0, 0x02, 0xE6, 0xFD]) # INY, INC
    code.extend([0xA5, 0xF2, 0xD0, 0x02, 0xC6, 0xF3, 0xC6, 0xF2]) # DEC 16
    code.extend([0xA5, 0xF2, 0x05, 0xF3]) # ORA
    add_rel(code, 0xD0, fread_loop, base_addr)
    code += [0x60]

    # --- rle_decode ---
    rle_addr = base_addr + len(code)
    code += [0xA0, 0x00] # Y=0
    rle_token_loop = base_addr + len(code)
    code.extend([0xA5, 0x0C, 0x05, 0x0D])
    rle_done_branch_idx = len(code); code.extend([0xF0, 0x00])
    code.extend([0xB1, 0xF8, 0xAA, 0xE6, 0xF8, 0xD0, 0x02, 0xE6, 0xF9, 0x8A, 0xC9, 0x80]) # Token
    rle_bcs_repeat_idx = len(code); code.extend([0xB0, 0x00])
    
    # Literal path
    code.extend([0x18, 0x69, 0x01, 0xAA]) # Lit Count
    rle_lit_loop = base_addr + len(code)
    code.extend([0xB1, 0xF8, 0x48, 0xE6, 0xF8, 0xD0, 0x02, 0xE6, 0xF9, 0x68]) # Read src
    code.extend([0x84, 0x10, 0xA0, 0x00, 0x91, 0xFC, 0xC8, 0xD0, 0x02, 0xE6, 0xFD, 0xA4, 0x10]) # Write dest
    code.extend([0xA5, 0x0C, 0xD0, 0x02, 0xC6, 0x0D, 0xC6, 0x0C]) # DEC uncomp
    code.extend([0xA5, 0x0C, 0x05, 0x0D, 0xF0, 0x07]) # If zero, jump to end of loop
    code.extend([0xCA]) # DEX
    add_rel(code, 0xD0, rle_lit_loop, base_addr)
    code.extend([0x4C, rle_token_loop & 0xFF, rle_token_loop >> 8])

    # Repeat path
    rle_rep_start = base_addr + len(code)
    code[rle_bcs_repeat_idx + 1] = (rle_rep_start - (base_addr + rle_bcs_repeat_idx + 2)) & 0xFF
    code.extend([0x38, 0xE9, 0x7E, 0xAA, 0xB1, 0xF8, 0x48, 0xE6, 0xF8, 0xD0, 0x02, 0xE6, 0xF9, 0x68])
    rle_rep_loop = base_addr + len(code)
    code.extend([0x48, 0x84, 0x10, 0xA0, 0x00, 0x91, 0xFC, 0xC8, 0xD0, 0x02, 0xE6, 0xFD, 0xA4, 0x10, 0x68])
    code.extend([0xA5, 0x0C, 0xD0, 0x02, 0xC6, 0x0D, 0xC6, 0x0C])
    code.extend([0xA5, 0x0C, 0x05, 0x0D, 0xF0, 0x04]) # If zero, done
    code.extend([0xCA])
    add_rel(code, 0xD0, rle_rep_loop, base_addr)
    code.extend([0x4C, rle_token_loop & 0xFF, rle_token_loop >> 8])

    rle_done_addr = base_addr + len(code)
    code[rle_done_branch_idx + 1] = (rle_done_addr - (base_addr + rle_done_branch_idx + 2)) & 0xFF
    code += [0x60]

    # --- Main ---
    main_addr = base_addr + len(code)
    code[jmp_main_idx+1]=main_addr&0xFF; code[jmp_main_idx+2]=main_addr>>8
    # ACK + Frame Counter
    code.extend([0x2C, 0x09, 0xDE, 0x50, 0xFB, 0xA9, 0xFF, 0x8D, 0x0A, 0xDE])
    code.extend([0x2C, 0x09, 0xDE, 0x50, 0xFB, 0xA5, 0x0E, 0x8D, 0x0A, 0xDE, 0x2C, 0x09, 0xDE, 0x50, 0xFB, 0xA5, 0x0F, 0x8D, 0x0A, 0xDE])
    code.extend([0xE6, 0x0E, 0xD0, 0x02, 0xE6, 0x0F])
    # Mode/BG
    code.extend([0xA9, 0x04, 0x85, 0xFC, 0xA9, 0x02, 0x85, 0xFD, 0xA2, 0x04, 0xA0, 0x00, 0x20, fread_addr & 0xFF, fread_addr >> 8])
    # Banks
    code.extend([0xA5, 0x03, 0xD0, 0x0A, 0xA9, 0x60, 0x85, 0x06, 0xA9, 0x44, 0x85, 0x07, 0x4C])
    j_r = len(code); code.extend([0x00, 0x00])
    w0 = base_addr + len(code); code[w0-base_addr-8]=w0&0xFF; code[w0-base_addr-7]=w0>>8
    code.extend([0xA9, 0x20, 0x85, 0x06, 0xA9, 0x04, 0x85, 0x07])
    reads = base_addr + len(code); code[j_r]=reads&0xFF; code[j_r+1]=reads>>8

    def add_s(d_z, d_h, u_l, u_h):
        code.extend([0xA9, 0x04, 0x85, 0xFC, 0xA9, 0x02, 0x85, 0xFD, 0xA2, 0x04, 0xA0, 0x00, 0x20, fread_addr & 0xFF, fread_addr >> 8])
        code.extend([0xA9, 0x00, 0x85, 0xFC, 0xA9, 0x80, 0x85, 0xFD, 0xAE, 0x04, 0x02, 0xAC, 0x05, 0x02, 0x20, fread_addr & 0xFF, fread_addr >> 8])
        code.extend([0xA9, 0x00, 0x85, 0xF8, 0xA9, 0x80, 0x85, 0xF9, 0xA9, 0x00, 0x85, 0xFC])
        if d_z: code.extend([0xA5, d_h, 0x85, 0xFD])
        else: code.extend([0xA9, d_h, 0x85, 0xFD])
        code.extend([0xA2, u_l, 0xA0, u_h, 0x86, 0x0C, 0x84, 0x0D, 0x20, rle_addr & 0xFF, rle_addr >> 8])

    add_s(True, 0x06, 0x40, 0x1F); add_s(True, 0x07, 0xE8, 0x03); add_s(False, 0xD8, 0xE8, 0x03)
    # VIC Init + Bank Switch
    code.extend([0xAD, 0x01, 0x02, 0x8D, 0x21, 0xD0, 0xAD, 0x02, 0x02, 0x8D, 0x22, 0xD0, 0xAD, 0x03, 0x02, 0x8D, 0x23, 0xD0, 0xAD, 0x01, 0x02, 0x8D, 0x20, 0xD0])
    code.extend([0xA9, 0x3B, 0x8D, 0x11, 0xD0, 0xA9, 0x18, 0x8D, 0x16, 0xD0, 0xA9, 0x18, 0x8D, 0x18, 0xD0])
    code.extend([0xAD, 0x00, 0xDD, 0x29, 0xFC, 0x85, 0x10, 0xA5, 0x03, 0x49, 0x01, 0x85, 0x03, 0x09, 0x02, 0x05, 0x10, 0x8D, 0x00, 0xDD])
    code.extend([0x4C, main_addr & 0xFF, main_addr >> 8])
    return bytes(code)

class KungFuFlashSerial:
    def __init__(self): self.ser = None
    def connect(self, p=None):
        if not p:
            ps = [pt.device for pt in serial.tools.list_ports.comports() if pt.vid == 0x0483 or 'serial' in pt.description.lower()]
            if not ps: return False
            p = ps[0]
        try: self.ser = serial.Serial(p, 115200, timeout=1); return True
        except: return False
    def send_viewer_prg(self):
        prg = bytearray([0x01,0x08,0x0B,0x08,0x0A,0x00,0x9E,0x32,0x30,0x36,0x31,0x00,0x00,0x00])
        prg.extend(_build_streamer_code(0x080D))
        try:
            self.ser.write(b'EFSTART:PRG\x00')
            if self.ser.read(1) != b'L': return False
            o = 0
            while o < len(prg):
                req = self.ser.read(2); sz = req[0] + req[1]*256
                ch = prg[o:o+sz]; self.ser.write(struct.pack('<H', len(ch)) + ch); o += len(ch)
            return True
        except: return False
    def stream_frame(self, m, bg, b1, b2, bitmap, screen, color):
        if not self.ser: return False
        try:
            ack = self.ser.read(3)
            if not ack or ack[0] != 0xFF:
                for _ in range(500):
                    if self.ser.read(1) == b'\xFF': self.ser.read(2); break
                else: return False
            self.ser.write(struct.pack('<HHBBBB', 4, 4, m, bg, b1, b2))
            for data in [bitmap, screen, color]:
                comp = rle_compress(data)
                self.ser.write(struct.pack('<HHH', 4, len(comp), 0))
                time.sleep(0.001)
                self.ser.write(struct.pack('<H', len(comp)) + comp)
            return True
        except: return False

async def main():
    s = KungFuFlashSerial()
    async def h(ws):
        async for msg in ws:
            if isinstance(msg, bytes):
                res = s.stream_frame(msg[0], msg[1], msg[8002], msg[9002], msg[2:8002], msg[8002:9002], msg[9002:10002])
                await ws.send(json.dumps({'success':res}))
            else:
                d = json.loads(msg); c = d.get('command')
                if c == 'connect': await ws.send(json.dumps({'success':s.connect(d.get('port'))}))
                elif c == 'send_viewer': await ws.send(json.dumps({'success':s.send_viewer_prg()}))
    async with websockets.serve(h, "localhost", 8765): await asyncio.Future()

if __name__ == "__main__": asyncio.run(main())
